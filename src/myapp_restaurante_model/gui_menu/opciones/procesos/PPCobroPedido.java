    private void jButtonPdfMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonPdfMouseClicked
        // TODO add your handling code here:pdf
        String idCliente = lTxt.getInformationIndividual(lTxt.getInformattionById(result, jTextCodigo.getText()))[2];
        if (jTextCodigo.getText().isEmpty() && lTxt.isId(result, idCliente)) {
            JOptionPane.showMessageDialog(null, "Coloca correctamente los datos");
        } else {
            action();
        }

    }//GEN-LAST:event_jButtonPdfMouseClicked
    public void action() {
        double valorPagar = Double.parseDouble(jTextPagar.getText());
        //obtengo los datos del pedido

        String fecha = lTxt.getInformationIndividual(lTxt.getInformattionById(result, jTextCodigo.getText()))[1];
        
        String valor = lTxt.getInformationIndividual(lTxt.getInformattionById(result, jTextCodigo.getText()))[3];
        String valoritbis = lTxt.getInformationIndividual(lTxt.getInformattionById(result, jTextCodigo.getText()))[4];
        String valorpropina = lTxt.getInformationIndividual(lTxt.getInformattionById(result, jTextCodigo.getText()))[5];
        
        double subtotal = Double.parseDouble(valor) + Double.parseDouble(valorpropina);
        double vitbis = Double.parseDouble(valoritbis);
        double total = subtotal + vitbis;
        //condicion para que la suma de dinero a pagar sea mayor
        if (total < Integer.parseInt(jTextPagar.getText())) {
            //genera el pdf
            gpdf = new GeneradorPDF();
            gpdf.createPDF(GeneradorPDF.generarFactura(jTextCodigo.getText(), fecha, subtotal, vitbis, total, valorPagar));
            
            try {
                //Ponemos a "Dormir" el programa durante los ms que queremos
                Thread.sleep(5 * 100);
                lTxt.getPdf("src\\myapp_restaurante_model\\pdf\\Pedido.pdf");
            } catch (Exception e) {
                System.out.println(e);
            }
            
        } else {
            JOptionPane.showMessageDialog(null, "La suma a pagar es inferior");
            
        }
    }
